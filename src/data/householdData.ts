export type HouseholdDataType = {
  identifier: string;
  purpose: string;
  planning: boolean;
  entirePeriod: boolean;
  data: { [key: string]: number };
  mgtg: string;
  isHidden?: boolean;
  uuid: string;
}[];

export const filterDataByYearAndMgtg = (
  data: HouseholdDataType,
  yearRange: [number, number],
  planning: boolean,
  sort?: 'ascending' | 'descending',
): HouseholdDataType => {
  const [startYear, endYear] = yearRange;

  const baseData = data
    .filter(
      (item) => relevantMgtg.includes(item.mgtg) && item.planning === planning,
    )
    .map((item) => {
      const filteredData = Object.fromEntries(
        Object.entries(item.data).filter(([year, value]) => {
          const yearInt = parseInt(year);
          return yearInt >= startYear && yearInt <= endYear && value > 0;
        }),
      );

      return {
        ...item,
        data: filteredData,
      };
    })
    .filter((item) => Object.keys(item.data).length > 0);

  const sumObjectValues = (obj: object) => {
    return Object.values(obj).reduce((a, c) => Number(a) + Number(c), 0);
  };

  const sortHouseholdData = (
    d: HouseholdDataType,
    sortOrder: 'ascending' | 'descending',
  ) => {
    return d.sort((a, b) => {
      const sumA = sumObjectValues(a.data);
      const sumB = sumObjectValues(b.data);
      if (sortOrder === 'ascending') {
        return sumA - sumB;
      } else {
        // This handles both descending and any other case
        return sumB - sumA;
      }
    });
  };

  if (sort) {
    sortHouseholdData(baseData, sort);
  }

  return baseData;
};

type HouseholdDataTotalType = {
  [key: string]: number | undefined;
};

type YearRange = [number, number];

export const getHouseholdSumFast = (
  data: HouseholdDataTotalType,
  range: YearRange,
): number => {
  const [startYear, endYear] = range;
  let sum = 0;

  for (let year = startYear; year <= endYear; year++) {
    sum += data[year.toString()] || 0;
  }

  return Math.round(sum / 1000);
};

